async function loadFragment(id, url) {
  const placeholder = document.getElementById(id);
  if (!placeholder) return;

  const shouldFetch = location.protocol !== "file:";
  if (shouldFetch) {
    try {
      const response = await fetch(url);
      if (response.ok) {
        placeholder.innerHTML = await response.text();
        return;
      }
      console.warn(`Failed to load ${url}: ${response.status}`);
    } catch (error) {
      console.warn(`Unable to fetch ${url}; falling back to embedded fragment.`, error);
    }
  }

  if (fragmentTemplates[id]) {
    placeholder.innerHTML = fragmentTemplates[id];
  }
}

function setActiveNavigation() {
  const currentPage = document.body.dataset.page;
  if (!currentPage) return;
  document.querySelectorAll(".navigation a").forEach((link) => {
    const href = link.getAttribute("href");
    const isActive = href === `${currentPage}.html` || (currentPage === "index" && href === "index.html");
    link.classList.toggle("active", isActive);
  });
}

function setLanguage(lang) {
  currentLanguage = lang;
  const localized = translations[lang];
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (localized[key]) {
      const value = localized[key];
      if (/<[^>]+>/.test(value)) {
        element.innerHTML = value;
      } else {
        element.textContent = value;
      }
    }
  });

  const langSwitchBtn = document.getElementById("langSwitch");
  if (langSwitchBtn) {
    langSwitchBtn.textContent = lang === "fi" ? "EN" : "FI";
  }

  localStorage.setItem("squashoulu-lang", lang);
}

function initLanguageSwitch() {
  const langSwitchBtn = document.getElementById("langSwitch");
  if (!langSwitchBtn) return;

  langSwitchBtn.addEventListener("click", () => {
    const newLang = currentLanguage === "fi" ? "en" : "fi";
    setLanguage(newLang);
    if (window.initEvents) {
      window.initEvents();
    }
  });

  setLanguage(currentLanguage);
}

async function initPage() {
  await Promise.all([
    loadFragment("siteHeader", "includes/header.html"),
    loadFragment("siteEvents", "includes/events.html"),
    loadFragment("siteFooter", "includes/footer.html"),
  ]);
  setActiveNavigation();
  initLanguageSwitch();
  renderSponsors();
  // Dynamically load events loader script and initialize events
  const script = document.createElement("script");
  script.src = "js/events.js";
  script.defer = true;
  script.onload = () => {
    if (window.initEvents) {
      try {
        window.initEvents();
      } catch (err) {
        console.error("initEvents failed:", err);
      }
    }
  };
  document.body.appendChild(script);
}

document.addEventListener("DOMContentLoaded", () => {
  initPage().catch((error) => console.error("Page initialization failed:", error));
});
